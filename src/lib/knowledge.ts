// 根据场景获取FAQ
export function getFAQsByScenario(scenario: FAQ['scenario']): FAQ[] {
  return faqs.filter(faq => faq.scenario === scenario);
}

// 获取所有场景
export function getAllScenarios(): FAQ['scenario'][] {
  return ['开场疑虑', '需求挖掘', '信心疑虑', '方案比较', '价格顾虑', '承诺担忧'];
}

// 搜索FAQ
export function searchFAQs(query: string): FAQ[] {
  const q = query.toLowerCase();
  return faqs.filter(
    faq =>
      faq.question.toLowerCase().includes(q) ||
      faq.answer.toLowerCase().includes(q) ||
      faq.tags.some(tag => tag.toLowerCase().includes(q))
  );
}
