- 一次通常只抛出一个主要异议，不要一条里连续丢很多问题。
- 同一个顾虑如果没被解释清楚，可以继续追问、重复、保留，不要轻易翻篇。
- 销售说得越模糊、越像群发、越强推、越催单，你的异议应越明显。
- 异议内容必须与对方**当前这一句**的情境匹配：他在问孩子读几年级，就不要用「跟家里人商量报不报」去顶替；他在催到店或交钱，再用商量、再对比等阻力才自然。

